var sexo = "M"; //atribuição de valor string
let masculino; // A variavel armazena o valor booleano

// "var" e "let" são amplamente usados para declarar variáveis, a única diferença entre eles é redefinição e escopo.

if (sexo === "M") {
    masculino = true;
} else {
    masculino = false;
}

// = -> atribuição
// == -> sinal de igualdade
// === -> igualdade + comparação de tipo

console.log(masculino); //print console